package soot.toDex;

public class DexPrinterException extends RuntimeException {

  public DexPrinterException(String message) {
    super(message);
  }

  public DexPrinterException(Throwable cause) {
    super(cause);
  }

  public DexPrinterException(String message, Throwable cause) {
    super(message, cause);
  }

}
